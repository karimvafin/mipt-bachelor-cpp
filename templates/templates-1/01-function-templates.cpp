// ============================================================
// Шаблоны функций (Function Templates)
// ============================================================
// Темы:
//   - Вывод типов (type deduction)
//   - Явная инстанцирование (explicit instantiation)
//   - Нетиповые параметры шаблона (non-type template parameters)
//   - Перегрузка шаблонных функций
// Компиляция: g++ -std=c++17 -Wall -o 01-function-templates 01-function-templates.cpp
// ============================================================

#include <iostream>
#include <string>
#include <array>

// ============================================================
// 1. Простой шаблон функции: max<T>
// ============================================================
// Компилятор генерирует отдельную функцию для каждого типа T,
// с которым вызывается шаблон. Это происходит во время компиляции.

template <typename T>
T my_max(T a, T b) {
    return (a > b) ? a : b;
}

// ============================================================
// 2. Шаблон с двумя параметрами типа: print_pair<T, U>
// ============================================================

template <typename T, typename U>
void print_pair(const T& first, const U& second) {
    std::cout << "(" << first << ", " << second << ")\n";
}

// ============================================================
// 3. Шаблон swap<T>
// ============================================================
// Стандартная библиотека уже предоставляет std::swap,
// но реализуем свой для наглядности.

template <typename T>
void my_swap(T& a, T& b) {
    T temp = a;
    a = b;
    b = temp;
}

// ============================================================
// 4. Когда вывод типа не работает: нужна явная инстанция
// ============================================================
// Компилятор не может вывести T, если аргументы имеют разные типы.
// Решение: явно указать тип в угловых скобках: my_max<double>(1, 2.5)

template <typename T>
T add(T a, T b) {
    return a + b;
}

// ============================================================
// 5. Нетиповые параметры шаблона (non-type template parameters)
// ============================================================
// Параметром шаблона может быть не только тип, но и константа времени
// компиляции: целое число, указатель, перечисление и т.д.

// Функция, которая возвращает N-й элемент массива с проверкой на этапе
// компиляции (если N >= Size, программа не скомпилируется).
// std::array использует std::size_t для параметра размера, поэтому
// N и Size объявлены как std::size_t.
template <std::size_t N, typename T, std::size_t Size>
T get_element(const std::array<T, Size>& arr) {
    static_assert(N < Size, "Индекс выходит за границы массива");
    return arr[N];
}

// Шаблон, создающий строку из N повторений символа (вычисления на этапе
// компиляции через рекурсию шаблонов — классический приём до C++17).
template <int N>
void print_line(char c = '-') {
    for (int i = 0; i < N; ++i) {
        std::cout << c;
    }
    std::cout << '\n';
}

// ============================================================
// 6. Перегрузка шаблонных функций
// ============================================================
// Обычные функции и шаблоны могут сосуществовать.
// Компилятор предпочитает точное совпадение нешаблонной функции.

// Общий шаблон: выводит тип и значение
template <typename T>
void describe(const T& value) {
    std::cout << "[шаблон] значение = " << value << "\n";
}

// Специализированная перегрузка для const char* (нешаблонная функция)
// Эта функция будет выбрана предпочтительно при передаче строкового литерала.
void describe(const char* value) {
    std::cout << "[строка] \"" << value << "\"\n";
}

// Перегрузка для двух аргументов
template <typename T>
void describe(const T& a, const T& b) {
    std::cout << "[шаблон, 2 арг] " << a << " и " << b << "\n";
}

// ============================================================
// 7. Явное инстанцирование (explicit instantiation)
// ============================================================
// Заставляет компилятор сгенерировать конкретную специализацию
// в данной единице трансляции. Полезно для ускорения сборки
// и контроля за тем, какие специализации существуют.

// Объявление явного инстанцирования:
template int my_max<int>(int, int);
template double my_max<double>(double, double);

// ============================================================
// main
// ============================================================

int main() {
    std::cout << "=== 1. my_max<T>: вывод типа ===\n";
    // Компилятор выводит T = int из аргументов
    std::cout << "my_max(3, 7)       = " << my_max(3, 7) << "\n";
    // Компилятор выводит T = double
    std::cout << "my_max(3.14, 2.71) = " << my_max(3.14, 2.71) << "\n";
    // Компилятор выводит T = std::string
    std::cout << "my_max(\"abc\",\"xyz\")= "
              << my_max(std::string("abc"), std::string("xyz")) << "\n";

    std::cout << "\n=== 2. print_pair<T, U>: два параметра типа ===\n";
    print_pair(42, 3.14);
    print_pair(std::string("hello"), true);
    print_pair('A', 65);

    std::cout << "\n=== 3. my_swap<T> ===\n";
    int x = 10, y = 20;
    std::cout << "До swap: x=" << x << ", y=" << y << "\n";
    my_swap(x, y);
    std::cout << "После swap: x=" << x << ", y=" << y << "\n";

    std::cout << "\n=== 4. Явное указание типа (explicit instantiation) ===\n";
    // Ошибка компиляции без явного указания типа:
    //   my_max(1, 2.5);  // int и double — T неоднозначен
    // Решение: явно указать тип
    std::cout << "my_max<double>(1, 2.5) = " << my_max<double>(1, 2.5) << "\n";
    std::cout << "add<double>(1, 2)      = " << add<double>(1, 2) << "\n";

    std::cout << "\n=== 5. Нетиповые параметры шаблона ===\n";
    std::array<int, 5> arr = {10, 20, 30, 40, 50};
    std::cout << "arr[0] = " << get_element<0>(arr) << "\n";
    std::cout << "arr[3] = " << get_element<3>(arr) << "\n";
    // get_element<10>(arr);  // ошибка компиляции: static_assert провалится

    std::cout << "Линия из 20 символов '-': ";
    print_line<20>();
    std::cout << "Линия из 10 символов '*': ";
    print_line<10>('*');

    std::cout << "\n=== 6. Перегрузка шаблонных функций ===\n";
    describe(42);             // выбирается шаблон describe<int>
    describe(3.14);           // выбирается шаблон describe<double>
    describe("hello");        // выбирается нешаблонная describe(const char*)
    describe(1, 2);           // выбирается двух-аргументный шаблон
    describe(1.0, 2.0);       // выбирается двух-аргументный шаблон

    std::cout << "\n=== Итог ===\n";
    std::cout << "Шаблоны функций позволяют писать обобщённый код,\n"
              << "который компилятор инстанцирует для конкретных типов.\n";

    return 0;
}
