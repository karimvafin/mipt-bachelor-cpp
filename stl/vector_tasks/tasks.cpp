// -----------------------------------------------------------------------------
// Задание 1 (Лёгкое): sum
// Посчитайте сумму всех элементов вектора.
// Пример: sum({1, 2, 3, 4}) == 10
// Пример: sum({}) == 0
// -----------------------------------------------------------------------------
int sum(const std::vector<int>& v) {
    int result = 0;
    for (auto i = v.begin(); i != v.end(); ++i) {
    result += *i;
    }
    return result;
}

