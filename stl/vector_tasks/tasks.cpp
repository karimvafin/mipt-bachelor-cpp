// -----------------------------------------------------------------------------
// Задание 1: sum (0.25 баллов)
// Посчитайте сумму всех элементов вектора.
// Пример: sum({1, 2, 3, 4}) == 10
// Пример: sum({}) == 0
// -----------------------------------------------------------------------------
int sum(const std::vector<int>& v) {
    int total_sum=0;
    for(size_t i=0; i<v.size();i++){
        total_sum=total_sum+v[i];
    }
    return total_sum;
}

